class ToMeAttention(Attention):
    """
    Modifications:
     - Apply proportional attention
     - Return the mean of k over heads from attention
    """

    def forward(
        self, x: torch.Tensor, size: torch.Tensor = None
    ) -> Tuple[torch.Tensor, torch.Tensor]:
        # Note: this is copied from timm.models.vision_transformer.Attention with modifications.
        B, N, C = x.shape
        qkv = (
            self.qkv(x)
            .reshape(B, N, 3, self.num_heads, C // self.num_heads)
            .permute(2, 0, 3, 1, 4)
        )
        q, k, v = (
            qkv[0],
            qkv[1],
            qkv[2],
        )  # make torchscript happy (cannot use tensor as tuple)

        attn = (q @ k.transpose(-2, -1)) * self.scale

        # Apply proportional attention
        if size is not None:
            attn = attn + size.log()[:, None, None, :, 0]

        attn = attn.softmax(dim=-1)
        attn = self.attn_drop(attn)

        x = (attn @ v).transpose(1, 2).reshape(B, N, C)
        x = self.proj(x)
        x = self.proj_drop(x)

        # Return k as well here
        return x, k.mean(1)


def white_noise_tens(z, snr_lin):

     with torch.no_grad():
         z_clone = z.clone().detach()  # Clone and detach tensor
         #x_power = torch.sum(z_clone**2) / torch.numel(z_clone)  # Signal power
         x_power= 0.2489
         if snr_lin is None:
             # No noise injection
             return z_clone
         x_power = torch.tensor(0.2489,dtype=z.dtype,device=z.device)
         var = x_power / snr_lin  # Variance based on SNR
         std = torch.sqrt(var)  # Standard deviation
         noise = torch.randn_like(z_clone) * std  # White noise
         return z_clone + noise  # Add noise to the original signal



def white_noise_norm(z, snr_lin):

    with torch.no_grad():
        z_clone = z.clone().detach()  # Clone and detach tensor

        # Calculate mean and standard deviation for normalization
        mean = torch.mean(z_clone)
        std = torch.std(z_clone)

        # Normalize to zero mean and unit standard deviation
        z_normalized = (z_clone - mean) / std

        if snr_lin is None:
            # No noise injection
            return z_clone
        
        if not isinstance(snr_lin, torch.Tensor):
            snr_lin = torch.tensor(snr_lin, dtype=z.dtype, device=z.device)

        # Add noise with unit variance
        noise = torch.randn_like(z_normalized) / torch.sqrt(snr_lin)
        z_noisy = z_normalized + noise
        # Scale back using the inverse of the normalization factor
        return z_noisy * std + mean

    
def _ntuple(n):
    def parse(x):
        if isinstance(x, collections.abc.Iterable) and not isinstance(x, str):
            return tuple(x)
        return tuple(repeat(x, n))
    return parse

def apply_patch(
    model: VisionTransformer, trace_source: bool = False, prop_attn: bool = True
):
    """
    Applies ToMe to this transformer. Afterward, set r using model.r.

    If you want to know the source of each token (e.g., for visualization), set trace_source = true.
    The sources will be available at model._tome_info["source"] afterward.

    For proportional attention, set prop_attn to True. This is only necessary when evaluating models off
    the shelf. For trianing and for evaluating MAE models off the self set this to be False.
    """
    ToMeVisionTransformer = make_tome_class(model.__class__)

    model.__class__ = ToMeVisionTransformer
    model.r = 0
    model._tome_info = {
        "r": model.r,
        "size": None,
        "source": None,
        "trace_source": trace_source,
        "prop_attn": prop_attn,
        "class_token": model.cls_token is not None,
        "distill_token": False,
    }

    if hasattr(model, "dist_token") and model.dist_token is not None:
        model._tome_info["distill_token"] = True

    for module in model.modules():
        if isinstance(module, Block):
            module.__class__ = ToMeBlock
            module._tome_info = model._tome_info
        elif isinstance(module, Attention):
            module.__class__ = ToMeAttention
            
            

def new_apply_patch(
    model: VisionTransformer, trace_source: bool = False, prop_attn: bool = True
):
    """
    Applies ToMe to the first half of the transformer blocks.
    Afterward, set r using model.r.

    If you want to know the source of each token (e.g., for visualization), set trace_source = true.
    The sources will be available at model._tome_info["source"] afterward.

    For proportional attention, set prop_attn to True. This is only necessary when evaluating models off
    the shelf. For training and for evaluating MAE models off the shelf set this to be False.
    """
    ToMeVisionTransformer = make_tome_class(model.__class__)

    model.__class__ = ToMeVisionTransformer
    model.r = 0
    model._tome_info = {
        "r": model.r,
        "size": None,
        "source": None,
        "trace_source": trace_source,
        "prop_attn": prop_attn,
        "class_token": model.cls_token is not None,
        "distill_token": False,
    }

    if hasattr(model, "dist_token") and model.dist_token is not None:
        model._tome_info["distill_token"] = True

    # Get the total number of blocks
    total_blocks = len([m for m in model.modules() if isinstance(m, Block)])
    half_blocks = total_blocks // 2

    # Counter for the number of blocks processed
    block_counter = 0

    for module in model.modules():
        if isinstance(module, Block):
            if block_counter < half_blocks:
                # Apply ToMeBlock only to the first half of blocks
                module.__class__ = ToMeBlock
                module._tome_info = model._tome_info
            if block_counter == half_blocks-1:
                module.__class__ = ToMeBlockMLP
            else:
                module.__class__ = ToMeBlocknoMerge
                module._tome_info = model._tome_info
            block_counter += 1
        elif isinstance(module, Attention):
            module.__class__ = ToMeAttention



